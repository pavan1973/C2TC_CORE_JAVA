package assignment;

public class switchCase_string {

	public static void main(String[] args) {
		String x = "vinuth";
		switch (x)
		{
		case "pavan":
		
			System.out.println("the char of x is pavan");
			break;
		
		case "vinuth":
			System.out.println("the char of x is vinuth");
			break;
		
		case "anil":
			System.out.println("the char of x is anil");
		break;
		default:
			System.out.println("invalid");
			break;
		}

	}

}
