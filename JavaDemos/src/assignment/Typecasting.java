package assignment;

public class Typecasting {

	public static void main(String[] args) {
	       float f = 10.532f;
	       long l = (long) f;
	       System.out.println(f);
	       System.out.println(l);
	       
	       int myint = 10;
	       double md = myint ;
	       System.out.println(myint);
	       System.out.println(md);


	}

}